int color(int x) {
    if (x == 0)  return 0;
    if (x == 22) return 46;
    if (x == 94) return 202;
    return 15;
}

void dote(int k) // 新しく関数を作る
{
    printf("\e[H\e[J"); // 画面消去
    for (int i = 0; i < 16; i++) {
        printf("%*s", k, ""); // k個の空白
        for (int j = 0; j < 16; j++) {
            int x = a[i][j];
            if (k % 2 == 1) { x = b[i][j]; }
            printf("\e[48;5;%dm", color(x));
            printf(""); printf("%2s", "");
            printf("\e[0m");
        }
        printf("\n");
    }
    usleep(125000); // 0.125秒
}
